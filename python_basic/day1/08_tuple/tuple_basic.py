##################################################

# 튜플 생성
point = (10, 20)  # 2D 좌표
rgb = (255, 0, 0)  # 빨간색
person = ("김철수", 25, "서울")

# 인덱싱과 슬라이싱 (리스트처럼)
print(point[0])    # 10
print(rgb[1:])     # (0, 0)

# 언패킹 (값 꺼내기)
x, y = point
print(x, y)  # 10 20

name, age, city = person
print(f"{name}은 {age}살이고 {city}에 살아요")

# 튜플은 수정 불가!
# point[0] = 30  # ❌ 에러!
# point.append(30)  # ❌ 에러!

# 하나짜리 튜플 (쉼표 필수!)
single = (5,)   # 튜플
not_tuple = (5)  # 그냥 숫자