##################################################
# 정수와 실수
##################################################

# 정수 (int)
count = 10
year = 2026

# 실수 (float)
pi = 3.14
temperature = 36.5

# 큰 숫자는 언더스코어로 구분 가능
population = 51_000_000  # 5100만
print(population)  # 51000000


##################################################
# 산술 연산자
##################################################

# 기본 연산자
print(10 + 3)   # 13 (더하기)
print(10 - 3)   # 7  (빼기)
print(10 * 3)   # 30 (곱하기)
print(10 / 3)   # 3.333... (나누기)

# 특별한 연산자
print(10 // 3)  # 3  (몫)
print(10 % 3)   # 1  (나머지)
print(10 ** 3)  # 1000 (거듭제곱)


##################################################
# 복합 연산자
##################################################

# 기본 방식 vs 복합 연산자
score = 80

# 기본 방식
score = score + 10
print(score)  # 90

# 복합 연산자 (같은 결과!)
score = 80
score += 10
print(score)  # 90