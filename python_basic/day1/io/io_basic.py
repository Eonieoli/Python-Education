# 기본 사용
name = input("이름을 입력하세요: ")
print(f"안녕하세요, {name}님!")

# 실행하면?
# 이름을 입력하세요: ▮  (입력 대기)
# (사용자가 "철수" 입력)
# 안녕하세요, 철수님!

# 숫자처럼 보이지만...
age = input("나이를 입력하세요: ")  # "25" 입력
print(type(age))  # <class 'str'> - 문자열!

# 숫자로 변환 - int()
age = input("나이를 입력하세요: ")  # "25" 입력
age = int(age)  # 문자열 "25"를 숫자 25로 변환
next_year = age + 1
print(f"내년엔 {next_year}살이네요!")

# 한 줄로
age = int(input("나이를 입력하세요: "))
result = age * 2
print(f"두 배는 {result}살!")