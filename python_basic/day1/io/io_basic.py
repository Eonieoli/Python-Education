# 기본 사용
print("Hello World")
print("안녕하세요!")

# 예제 2: 여러 개 출력 (쉼표로 구분)
print("이름:", "김철수")
print("나이:", 25, "살")
# 출력: 이름: 김철수
# 출력: 나이: 25 살

# 예제 3: 계산 결과 출력
print(10 + 20)        # 30
print(5 * 3)          # 15

# 예제 4: 변수 출력
name = "이영희"
age = 30
print(name)           # 이영희
print(name, age)      # 이영희 30
print(f"{name}님은 {age}살입니다")  # 이영희님은 30살입니다


