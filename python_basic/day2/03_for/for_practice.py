# 연습 1: 사용자 목록 처리하기
##################################################

# 사용자 목록 (딕셔너리의 리스트!)
users = [
    {"name": "김철수", "age": 25, "city": "서울"},
    {"name": "이영희", "age": 30, "city": "부산"},
    {"name": "박민수", "age": 28, "city": "대구"}
]

# 모든 사용자 정보 출력
for user in users:
    print(f"이름: {user['name']}, 나이: {user['age']}, 도시: {user['city']}")

# 출력:
# 이름: 김철수, 나이: 25, 도시: 서울
# 이름: 이영희, 나이: 30, 도시: 부산
# 이름: 박민수, 나이: 28, 도시: 대구

# 각 사용자 상세 정보
for user in users:
    print(f"\n{user['name']}님의 정보:")
    for key, value in user.items():
        print(f"  {key}: {value}")


