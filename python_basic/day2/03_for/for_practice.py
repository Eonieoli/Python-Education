# 연습 2: 숫자 범위 반복
##################################################

# 1부터 10까지 출력
for i in range(1, 11):
    print(i)

# 짝수만 출력 (0부터 10까지)
for i in range(0, 11, 2):
    print(i)

# 출력: 0 2 4 6 8 10

# 카운트다운
print("카운트다운!")
for i in range(10, 0, -1):
    print(i)
print("발사!")

# 별 피라미드
for i in range(1, 6):
    print("*" * i)

# 출력:
# *
# **
# ***
# ****
# *****


##################################################
# 연습 3: 중첩 for 문 (2차원 리스트 출력하기)
##################################################

# 2차원 리스트 (리스트 안에 리스트)
matrix = [
    [1, 2, 3],
    [4, 5, 6],
    [7, 8, 9]
]

# 2차원 리스트 출력
for row in matrix:
    for num in row:
        print(num, end=' ')
    print()  # 줄바꿈

# 출력 결과:
# 1 2 3 
# 4 5 6 
# 7 8 9


##################################################
# 연습 4: 중첩 for 문 (구구단 전체 출력)
##################################################

# 2단부터 9단까지 전체 출력
for dan in range(2, 10):
    for num in range(1, 10):
        print(f"{dan} x {num} = {dan * num}")
    print()  # 단 사이 공백

# 출력 결과:
# 2 x 1 = 2
# ...
# 2 x 9 = 18
#              ← 빈 줄
# 3 x 1 = 3
# ...


##################################################
# 연습 5: 중첩 for 문 (좌표 쌍 만들기)
##################################################

# (x, y) 좌표 쌍 만들기
coordinates = []

for x in range(3):
    for y in range(3):
        coordinates.append((x, y))

print(coordinates)